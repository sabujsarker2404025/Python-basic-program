n = int(input("Enter a number: "))
match n:
  case 1:
     print(" sunday ")
  case 2:
     print("monday ")   
  case 3:
     print("Tuesday ")  
  case 4:
     print("wednesday ")  
  case 5:
     print("Thursday ")
  case 6:
     print("friday ")       
  case 7:
     print("Saturday ") 
  case _:
     print("Invalid ")               